package com.syntax.class01;

public class PracticeVariable {

	public static void main(String[] args) {
		// whole number byte, short, int, and long
		byte practiceOne=12;
		short practiceTwo=1200;
		int practiceThree=12000;
		long practiceFour=120000;
		System.out.println(practiceOne);
		System.out.println(practiceTwo);
		System.out.println(practiceThree);
		System.out.println(practiceFour);
		//Decimal number float or double
		float practice1st=12.99f;
		double practice2nd=12.99999999999;
		System.out.println(practice1st);
		System.out.println(practice2nd);
		// Non-numeric value 
		char p1st='F';
		char p2nd='%';
		System.out.println(p1st);
		System.out.println(p2nd);
		boolean money=true;
		boolean everythingelse=false;
		System.out.println(money);
		System.out.println(everythingelse);
		
		byte x=13;
		short y=13000;
		System.out.println(x);
		System.out.println(y);
		
		
				
		
		
		
		
				
		
		

	}

}
