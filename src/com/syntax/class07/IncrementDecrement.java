package com.syntax.class07;

public class IncrementDecrement {

	public static void main(String[] args) {
		// different way of writing plus 1 are: a= x +1, or x+=1, or x++; it same in case subtruction also
		int x=10;// to add number is called increment
		x=x+1;
		x+=1;//=(+1)
		x++;//=(+1)
		System.out.println(x);
		int y=90;// to subtruct number is called decrement
		y-=1;
		y--;
		y=y+1;
		System.out.println(y);

	}

}
